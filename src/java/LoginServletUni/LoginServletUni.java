    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
        
         String username = request.getParameter("username");
        String password = request.getParameter("password");
        
        LoginBeanUni loginBean = new LoginBeanUni();
        
        loginBean.setUsername(username);
        loginBean.setPassword(password);
        
        LoginDaoUni loginDao = new LoginDaoUni();
        
    
        
        String userValidate = loginDao.authenticateUser(loginBean);
    
        if(userValidate.equals("SUCCESS"))
        {
            request.setAttribute("username", username);
            request.getRequestDispatcher("/user_panel.jsp").forward(request, response);
        }
        else
        {
            request.setAttribute("errMessage", userValidate);
            request.getRequestDispatcher("/customer_login.jsp").forward(request, response);
        }
    }
    

